import { mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const rootDir = dirname(dirname(fileURLToPath(import.meta.url)));
const tmpDir = join(rootDir, 'tmp', 'adapter-angular-styles');
const inputPath = join(tmpDir, 'tailwind-input.css');
const tailwindOutputPath = join(tmpDir, 'tailwind-output.css');
const baseStylesPath = join(rootDir, 'packages', 'adapter-angular', 'src', 'styles', 'base.css');
const outputPath = join(rootDir, 'packages', 'adapter-angular', 'src', 'styles.css');
const configPath = join(rootDir, 'packages', 'adapter-angular', 'tailwind.config.cjs');

mkdirSync(tmpDir, { recursive: true });
writeFileSync(inputPath, '@tailwind utilities;\n');

const npx = process.platform === 'win32' ? 'npx.cmd' : 'npx';
const result = spawnSync(
  npx,
  ['tailwindcss', '-i', inputPath, '-o', tailwindOutputPath, '--config', configPath, '--minify'],
  {
    cwd: rootDir,
    stdio: 'inherit',
    shell: process.platform === 'win32'
  }
);

if (result.error) {
  console.error(result.error.message);
  process.exit(1);
}

if (result.status !== 0) {
  process.exit(result.status ?? 1);
}

const banner = [
  '/*',
  ' * Generated by tools/build-adapter-angular-styles.mjs.',
  ' * Do not edit this file directly; edit packages/adapter-angular/src/styles/base.css or the Angular templates.',
  ' */',
  ''
].join('\n');

const baseStyles = readFileSync(baseStylesPath, 'utf8').trim();
const tailwindStyles = readFileSync(tailwindOutputPath, 'utf8').trim();

writeFileSync(outputPath, `${banner}${baseStyles}\n\n${tailwindStyles}\n`);
rmSync(tmpDir, { recursive: true, force: true });
